    <h1 class=" leading-6">
Karatetræning er mere end blot selvforsvar. Det er en rejse, hvor kroppen og sindet trænes i harmoni. Gennem fysisk og mental træning lærer man at håndtere udfordrende situationer samtidig med, at man konstant stræber efter at forbedre sig selv.<br><br>

Budo SportsKarate Ishøj praktiserer Wado Ryu-stilen, skabt i 1934 af Sensei Hironori Othsuka. Den kombinerer Okinawansk karate med den gamle japanske Budo-stil, "Shindo Yoshin Ryu Jujitsu". W.I.K.F. blev etableret i 1990 af Sensei Tatsuo Suzuki for at bevare Othsuka's originale træningsmetoder.<br><br>

Karate handler om at forstå og bruge disciplinen korrekt, byggende på tanken om "en sund sjæl i et sundt legeme". Traditionel karate fremmer respekt, pli og god opførsel gennem fysisk og mental træning. Styrke findes ikke kun i fysikken, men også i tanken, og respekt opnås gennem behandlingen af andre.<br><br>

<span class=" font-bold">Karate indebærer visse dojo kun (principper):</span><br>

<ul class="list-disc leading-9">
    <li><span class=" font-bold">Reisetsu O Mamori:</span> Overhold altid reglerne.</li>
    <li><span class=" font-bold">Shingi O Omanji:</span> Vær loyal overfor Sensei.</li>
    <li><span class=" font-bold">Jojitsu Ni Oberezu:</span> Elev og sensei er ikke ligemænd.</li>
    <li><span class=" font-bold">Shinkenmi Ni Tesseyo:</span> Giv altid dit bedste.</li>
</ul>
<br>
Karate er en kampsport, potentiel farlig uden disciplin. Strikte regler sikrer sikkerhed under træningen. Høflighed og respekt er grundlæggende regler i karate, hvor målet er at blive bedre både som karateudøver og som menneske. Kom og vær med til at udvikle dig sammen med os.<br><br>

In pursuit of perfection.
    </h1>
